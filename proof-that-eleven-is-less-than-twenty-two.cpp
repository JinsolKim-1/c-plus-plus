#include <iostream>
using namespace std;

int main()
{
    int i = 11;
    int j = 22;


    cout << (i==j) << "\n";

    cout << (i<j) << "\n";

    cout << (i>j) << "\n";

    cout << (i!=j) << "\n";

    cout << (i>=j) << "\n";

    cout << (i<=j) << "\n";


    return 0;
}